    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = issueList.getSelectedRow();
        if (selectedRow != -1) {
            // Get the updated data from the text fields and combo box
            String updatedBook_ID = txtIssuebookID.getText();
            String updatedStudendId= txtIssuestudentID.getText();
            String updatedIssuebook =txtIssuebook.getText();
             Date updatedDuedate = duedateSelector.getDate();
           

            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to update the issued book?", "Confirm Update", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                // Update the data in the table (user interface)
                DefaultTableModel tableModel = (DefaultTableModel) issueList.getModel();
                tableModel.setValueAt(updatedBook_ID, selectedRow, 0);
                tableModel.setValueAt(updatedStudendId, selectedRow, 1);
                tableModel.setValueAt(updatedIssuebook, selectedRow, 2);
                tableModel.setValueAt(updatedDuedate, selectedRow, 3);
                

                // Update the data in the MySQL table
                try {
                    // Establish a database connection
                    Connection connection = Dbconnection.getConnection();

                    // Define the SQL query to update the record in the database
                    String sql = "UPDATE IssueBook SET Book_ID= ? Student_ID =?, IssueBook=?, Due_Date=? WHERE IssueBook_ID ";

                    // Prepare the statement
                    PreparedStatement preparedStatement = connection.prepareStatement(sql);
                    preparedStatement.setString(1, updatedIssuebook);
                   
                    preparedStatement.setDate(2, new java.sql.Date(updatedDuedate.getTime()));
                    preparedStatement.setString(3, updatedBook_ID);
                    preparedStatement.setString(4, updatedStudendId);

                    // Execute the update
                    int rowsAffected = preparedStatement.executeUpdate();

                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(this, "Issued books updated successfully.");
                    } else {
                        JOptionPane.showMessageDialog(this, "Failed to update issued books details in the database.");
                    }

                    // Close the database connection
                    connection.close();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select issued books details to update.");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

