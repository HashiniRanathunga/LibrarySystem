    private void displayExistingIssueBookData() {
    try {
        try ( // Establish a database connection
              Connection connection = Dbconnection.getConnection();) {
            String sql = "SELECT IssueBook_ID,Book_ID, Student_ID, IssueBook, Due_Date  FROM IssueBook";
            
            PreparedStatement preparedStatement = connection.prepareStatement(sql);
            ResultSet resultSet = preparedStatement.executeQuery();
            
            DefaultTableModel tableModel = (DefaultTableModel) issueList.getModel();
            
            while (resultSet.next()) {
                String BookID = resultSet.getString("BookID");
                String StudentID = resultSet.getString("StudentID");
                String IssueBook = resultSet.getString("IssueBook");
                Date DueDate = resultSet.getDate("DueDate");
                
                // Add the fetched data as a new row in the table
                tableModel.addRow(new Object[]{BookID, StudentID, IssueBook, DueDate});
            }
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
}
    
    
